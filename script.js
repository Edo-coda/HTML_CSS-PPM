/* Javascript image*/

let img = document.querySelector(".toggle_img");
let link = document.querySelector(".panel_link");

img.addEventListener("mouseover", function () {
    console.log("over");
    link.style.textDecoration = "underline #0b5ed7";
})

img.addEventListener("mouseleave", function () {
    console.log("leave");
    link.style.textDecoration = "none";
})

